def remaining_questions(questions, user_answer) -> list:
    """저장된 질문 중 사용자 답변에서 아직 채워지지 않은 질문만 반환.
    부분 답변(예: 3개 중 2개만 답)을 정확히 가려, 미답 질문만 남긴다."""
    if not questions:
        return []
    q_lines = "\n".join(f"{i}. {q}" for i, q in enumerate(questions, 1))
    prompt = (
        "아래 [질문 목록]에 대해 사용자가 [답변]을 했습니다. "
        "각 질문이 답변에서 실질적으로 채워졌는지 판단하세요.\n\n"
        f"[질문 목록]\n{q_lines}\n\n"
        f"[답변]\n{user_answer}\n\n"
        "[판단 기준]\n"
        "- 해당 질문에 대한 정보가 답변에 구체적으로 담겨 있으면 '답변됨'.\n"
        "- 답변에 언급이 없거나, 'TBD/모름/없음/나중에' 등 회피·보류면 '미답'.\n"
        "- 사용자가 해당 질문의 전제가 틀렸다고 하거나 질문이 잘못됐다고 정정하면(예: '그건 잘못된 정보야', 'X는 Y가 아니라 Z야', '그건 물어볼 필요 없어') → 그 질문은 '제거'한다(remaining에 넣지 않는다). 정정으로 사용자가 알려준 정보는 답변된 것으로 본다.\n"
        "- 사용자가 질문 순서대로 답하지 않을 수 있으니, 내용으로 매칭하세요.\n\n"
        "미답으로 남은 질문만(= 답변되지도, 정정·제거되지도 않은 것) 원문 그대로 JSON 배열로 출력하세요(번호 없이 질문 문장만). "
        "모두 답변됐으면 빈 배열.\n"
        'JSON으로만 출력: { "remaining": ["미답 질문 원문", ...] }'
    )
    res = bedrock.call_json(prompt, 10000)
    rem = (res or {}).get("remaining", [])
    # 안전장치: LLM이 엉뚱한 걸 반환하면 원본 질문 집합 내로 제한
    return [q for q in questions if q in rem] or [q for q in rem if q in questions] or rem



