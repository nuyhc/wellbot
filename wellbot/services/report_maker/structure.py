def check_deepdive_info(topic, analysis, proposed_structure, page_count, page_plan="") -> dict:
    """서머리 다장: 심층 페이지로 다룰 과제의 정보가 충분한지 점검. 부족하면 질문 생성."""
    try:
        deep_pages = int(float(page_count)) - 1  # 1장은 전체 서머리, 나머지가 심층
    except Exception:
        deep_pages = 1
    deep_target_note = (
        f"[심층으로 다룰 과제 — 사용자가 선택한 이 과제(들)만 심층 페이지로 작성한다]\n{page_plan}\n\n"
        if page_plan else ""
    )
    prompt = (
        "월간/주간 서머리 보고서를 작성하려 합니다. 1장은 전체 과제 요약이고,\n"
        f"나머지 {deep_pages}개 페이지는 강조 과제를 1개씩 골라 '심층(Deep-dive)'으로 작성합니다.\n\n"
        f"[원본 입력]\n{topic}\n\n"
        f"[파악 내용]\n{analysis}\n\n"
        f"[확정된 구조 — 심층 대상 과제 포함]\n{proposed_structure}\n\n"
        + deep_target_note +
        "[점검 목적]\n"
        "심층 페이지로 다룰 과제 각각에 대해, 임원 보고용 심층 1장을 채울 정보가 충분한지 판단하세요.\n"
        "심층 1장에는 보통 다음이 필요합니다: 추진 경과·현황, 기대효과 또는 성과, 향후 계획·일정, 핵심 수치.\n\n"
        "[판단 기준]\n"
        "- 심층 대상 과제의 정보가 1~2줄 수준으로만 있어 경과·효과·일정 중 상당수가 비어 있으면 → 부족(질문 필요).\n"
        "- 단정 시 위험한 민감 정보(인력·비용)나 핵심 수치가 비어 있으면 → 질문(지어내기 금지).\n"
        "- 이미 충분하면 → 질문 없이 진행.\n\n"
        "[질문 작성 시 — 반드시 준수]\n"
        "- ★ 질문은 오직 위 [심층으로 다룰 과제]에 명시된 과제(들)에 대해서만 한다. 전체 서머리에는 있지만 이번에 심층으로 고르지 않은 다른 과제는, 정보가 부족해도 묻지 않는다(그 과제들은 1장 서머리에 카테고리로만 들어가므로 심층 정보가 필요 없다). [심층으로 다룰 과제]가 비어 있으면 구조에서 '[심층]'으로 표시된 과제만 대상으로 한다.\n"
        "- 정말 필요한 항목만, 과제별로 묶어 최대 3개 이내.\n"
        "- 본부장 보고에 필요한 관점(그래서 무엇이 달라지는가, 어떤 효과·의사결정이 필요한가)에서 묻는다.\n"
        "- 사소한 보완은 묻지 말고 (TBD) 처리하도록 둔다.\n\n"
        "JSON으로만 출력:\n"
        '{ "sufficient": true 또는 false, '
        '"questions": ["부족할 때만, 과제명과 함께 질문 1~3개"], '
        '"deep_targets": ["심층으로 다룰 과제명 목록"] }'
    )
    return bedrock.call_json(prompt, 20000)


